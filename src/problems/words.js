const getWords = (amount = 25) => {
  let results = [];
  let takenBank = {};
  for (let i = 0; i < amount; i++) {
    const j = Math.floor(Math.random() * words.length);
    if (!takenBank[j]) {
      const word = words[j];
      results.push(makeQuestion(word));
      takenBank[j] = true;
    } else {
      // if we have selected the word already, deincrement i and try again;
      i -= 1;
    }
  }

  return results;
};

